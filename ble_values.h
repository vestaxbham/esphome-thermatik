static const std::map<std::string, uint16_t> HEX_TABLE = []()
{
    std::map<std::string, uint16_t> m;
    for (auto &e : HEX_TABLE_RAW)
    {
        auto res = m.emplace(e.name, e.value);
    }
    return m;
}();

constexpr const char *find_duplicate()
{
    constexpr size_t N = sizeof(HEX_TABLE_RAW) / sizeof(HexEntry);

    for (size_t i = 0; i < N; i++)
    {
        for (size_t j = i + 1; j < N; j++)
        {
            if (strcmp(HEX_TABLE_RAW[i].name, HEX_TABLE_RAW[j].name) == 0)
            {
                return HEX_TABLE_RAW[i].name; // duplicated key
            }
        }
    }
    return nullptr;
}

static_assert(find_duplicate() == nullptr, "Duplicate key found in HEX_TABLE_RAW");

// Public API
static inline std::vector<uint8_t> get_hex_bytes(const std::string &name)
{
    auto it = HEX_TABLE.find(name);
    if (it != HEX_TABLE.end())
    {
        uint16_t v = it->second;
        // Convert 0x1EF2 to {0xF2, 0x1E}
        return {uint8_t(v & 0xFF), uint8_t(v >> 8)};
    }
    // You decide how to handle missing keys
    ESP_LOGE("main", "BUG: Raw value %s doesnt exist in dictionary", name.c_str());
    return {0x00, 0x00};
}
